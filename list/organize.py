# Sort List permanently with the sort() method.

cars = ['bmw', 'benz', 'audi', 'deguaa', 'toyota']

cars.sort()
print('cars.sort()')
print(cars)

# reverse sort list
print('\ncars.sort(reverse=True)')
cars.sort(reverse=True)
print(cars)

# use this method instead
print('\nReverse order')
cars.reverse()
print(cars)